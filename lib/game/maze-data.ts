const MAZE_LAYOUTS: Record<number, number[][]> = {
  1: CLASSIC_MAZE_LAYOUT,
  2: LEVEL_2_LAYOUT,
  3: LEVEL_3_LAYOUT,
  4: LEVEL_4_LAYOUT,
  5: LEVEL_5_LAYOUT,
}

export function createMaze(level: number = 1): Maze {
  const layout = MAZE_LAYOUTS[level] || CLASSIC_MAZE_LAYOUT
  const height = layout.length
  const width = layout[0].length
  const cells: Cell[][] = []
  let totalPellets = 0

  for (let y = 0; y < height; y++) {
    cells[y] = []
    for (let x = 0; x < width; x++) {
      const value = layout[y][x]
      let type: CellType
      let hasPellet = false
      let hasPowerPellet = false

      switch (value) {
        case 0:
          type = "wall"
          break
        case 1:
          type = "path"
          hasPellet = true
          totalPellets++
          break
        case 2:
          type = "path"
          hasPowerPellet = true
          totalPellets++
          break
        case 3:
          type = "ghost_house"
          break
        case 4:
          type = "path"
          break
        default:
          type = "empty"
      }

      cells[y][x] = { type, x, y, hasPellet, hasPowerPellet }
    }
  }

  return {
    width,
    height,
    cells,
    pacmanStart: { x: 14, y: 23 },
    ghostHome: { x: 14, y: 14 },
    totalPellets,
  }
}

export function isWalkable(maze: Maze, position: Vector2D): boolean {
  const { x, y } = position
  if (x < 0 || x >= maze.width || y < 0 || y >= maze.height) {
    return false
  }
  const cell = maze.cells[y][x]
  return cell.type !== "wall"
}

export function getNeighbors(maze: Maze, position: Vector2D): Vector2D[] {
  const neighbors: Vector2D[] = []
  const directions = [
    { x: 0, y: -1 }, // up
    { x: 0, y: 1 }, // down
    { x: -1, y: 0 }, // left
    { x: 1, y: 0 }, // right
  ]

  for (const dir of directions) {
    const newPos = { x: position.x + dir.x, y: position.y + dir.y }

    // Túnel - wrap around
    if (newPos.x < 0) newPos.x = maze.width - 1
    if (newPos.x >= maze.width) newPos.x = 0

    if (isWalkable(maze, newPos)) {
      neighbors.push(newPos)
    }
  }

  return neighbors
}

export function getCellAt(maze: Maze, position: Vector2D): Cell | null {
  const { x, y } = position
  if (x < 0 || x >= maze.width || y < 0 || y >= maze.height) {
    return null
  }
  return maze.cells[y][x]
}
